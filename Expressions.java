public class Expressions {

    public static void main(String[] args) {

        int x = 20;
        System.out.println(x);
        x = x + 1; // addition
        System.out.println(x);

        x = x - 1;// subtraction
        System.out.println(x);

        x = x * 2;// multiplication
        System.out.println(x);

        x = x / 3;// divison
        System.out.println(x);

        x = x % 4;// remainder

        System.out.println(x);

    }
}
