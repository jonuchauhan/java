import java.lang.Math.*;

public class Math {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        int z = java.lang.Math.max(x, y);
        System.out.println("max of x and y is :-" + z);

        double a = java.lang.Math.sqrt(x * x + y * y);

        System.out.println("sqrt of both x and y is :-" + a);

    }

}
