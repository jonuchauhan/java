public class WrapperClass {

    public static void main(String[] args) {

        Boolean a = true;
        Integer x = 10;
        Double y = 3.145;
        Character z = '@';

        System.out.println(a.toString());
        System.out.println(x.toString());
        System.out.println(y.toString());
        System.out.println(z.charValue());

    }

}