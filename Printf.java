public class Printf {

    public static void main(String[] args) {

        boolean a = true;
        char b = '&';
        int c = 10;
        double d = 10.98;
        String e = "Hello";

        System.out.printf("This is my boolean value %b\n", a);
        System.out.printf("This is my character value %c\n", b);
        System.out.printf("This is my integer value %d\n", c);
        System.out.printf("This is my double  value %f\n", d);
        System.out.printf("This is my string value %s\n", e);

    }

}
